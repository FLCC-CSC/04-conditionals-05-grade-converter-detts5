# BRIEF DESCRIPTION:  This program asks the user for a percentage grade
# and converts it to the corresponding letter grade based on a standard scale.









print('===== Grade Converter =====')

percent = int(input('Enter a numerical grade (1-100): '))

if percent > 100:
    print('A+')
elif percent >= 90:
    print('A')
elif percent >= 80:
    print('B')
elif percent >= 70:
    print('C')
elif percent >= 65:
    print('D')
else:
    print('F')



    







